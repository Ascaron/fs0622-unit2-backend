
public class Articolo {

	int codiceArt;
	String descrizione;
	double prezzo;
	int numeroPezzi;
	
	public Articolo(int codiceArt, String descrizione, double prezzo, int numeroPezzi) {
		
		this.codiceArt= codiceArt;
		this.descrizione= descrizione;
		this.prezzo= prezzo;
		this.numeroPezzi=numeroPezzi;
	}
}